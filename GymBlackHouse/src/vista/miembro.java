    private void listar(){
        List<miembros> lista=dao.Listar();
        modelo=(DefaultTableModel) Tabla.getModel();
        Object[] ob=new Object[8];
        for(int i=0;i<lista.size();i++){
            ob[0]=lista.get(i).getId();
            ob[1]=lista.get(i).getRut();
            ob[2]=lista.get(i).getNombre();
            ob[3]=lista.get(i).getPaterno();
            ob[4]=lista.get(i).getMaterno();
            ob[5]=lista.get(i).getTaller();
            ob[6]=lista.get(i).getDias();
            ob[7]=lista.get(i).getCuotas();
            modelo.addRow(ob);

        }
        Tabla.setModel(modelo);
    }

