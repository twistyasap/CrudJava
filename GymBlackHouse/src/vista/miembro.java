    private void btnagregarActionPerformed(java.awt.event.ActionEvent evt) {                                           
        String rut = txtrut.getText();
        String nombre = txtnombre.getText();
        String paterno = txtpaterno.getText();
        String materno = txtmaterno.getText();
        String taller = txttaller.getText();
        String diasStr = (String) combobox.getSelectedItem();
        String cuotaStr = txtcuotas.getText();

    if (rut.isEmpty() || nombre.isEmpty() || paterno.isEmpty() || materno.isEmpty() || taller.isEmpty() || diasStr.isEmpty() || cuotaStr.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Rellene todos los campos", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    int dias = Integer.parseInt(diasStr);
    int cuota = Integer.parseInt(cuotaStr);

    m.setRut(rut);
    m.setNombre(nombre);
    m.setPaterno(paterno);
    m.setMaterno(materno);
    m.setTaller(taller);
    m.setDias(dias);
    m.setCuotas(cuota);

    boolean r = dao.insertar(m);

    if (r) {
        JOptionPane.showMessageDialog(this, "El miembro se registró con éxito");
        limpiar();
        listar();
    } else {
        JOptionPane.showMessageDialog(this, "Error al registrar");
    }
}

