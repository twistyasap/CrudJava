    private void btnmodificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnmodificarActionPerformed
        // TODO add your handling code here:
        int fila=Tabla.getSelectedRow();
        if(fila==-1){
             JOptionPane.showMessageDialog(rootPane, "Selecciona una fila de la tabla");
        }else{
            m.setId(Integer.parseInt(txtid.getText()));
            m.setRut(txtrut.getText());
            m.setNombre(txtnombre.getText());
            m.setPaterno(txtpaterno.getText());
            m.setMaterno(txtmaterno.getText());
            m.setTaller(txttaller.getText());
            m.setDias(Integer.parseInt((String) combobox.getSelectedItem()));
            m.setCuotas(Integer.parseInt(txtcuotas.getText()));
            if(dao.editar(m)){
                JOptionPane.showMessageDialog(rootPane, "Se ha modifico correctamente");
                limpiar();
                listar();
                txtid.setText("");
                txtrut.setText("");
                txtnombre.setText("");
                txtpaterno.setText("");
                txtmaterno.setText("");
                txttaller.setText("");
                
                txtcuotas.setText("");
            }else{
              JOptionPane.showMessageDialog(rootPane, "Error al modificar el miembro");  
            }
        }
    }//GEN-LAST:event_btnmodificarActionPerformed

